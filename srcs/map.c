void	select_map(t_wolf *wolf)
{
	DIR_X = -1;
	DIR_Y = 0;
	PLANX = 0;
	PLANY = 0.66;
	if (CHECK_MUSIC)
		system("killall afplay");
	if (MAP_NB == 1)
		map(wolf);
	else if (MAP_NB == 2)
	{
		POS_X = 10.5;
		POS_Y = 10.5;
		map2(wolf);
	}
	else if (MAP_NB == 3)
	{
		POS_X = 17.5;
		POS_Y = 17.5;
		map3(wolf);
	}
	if (CHECK_MUSIC)
		start_music(wolf);
}
